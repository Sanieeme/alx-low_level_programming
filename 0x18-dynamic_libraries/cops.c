#include <stdio.h>
#include <string.h>
/**
 * div - function that div two integers
 * @a: parameter
 * @b: parameter
 * Return: (a/b)
 */
int div(int a, int b)
{
	return (a/b);
}

/**
 * mod - function that mod two integers
 * @i: parameter
 * @j: parameter
 * Return: (i%j)
 */
int mod(int i, int j)
{
	return (i % j);
}

/**
 * mul - function that multiple two integers
 * @a: parameter
 * @b: parameter
 * Return: (a*b)
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * sub - functions that sub two integers
 * @i: parameter
 * @j: parameter
 * Return: i-j
 */
int sub(int i, int j)
{
	return (i - j);
}

/**
 * add - functions that add two integers
 * @i: parameter
 * @j: parameter
 * Return: i+j
 */
int add(int i, int j)
{
	return (i + j);
}
